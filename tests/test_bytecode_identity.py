"""Bytecode identity tests: compare our codegen output against reference
disassembly captured from tclsh.

Reference files live in ``tests/bytecode_reference/<version>/`` and are
generated by ``scripts/capture_reference_bytecode.sh``.  When no
reference files exist the tests are skipped.

The comparison normalises both outputs to extract only the instruction
stream (opcodes, operands, comments) so that metadata differences
(header format, source locations, exception ranges, command maps) do not
cause spurious failures.
"""

from __future__ import annotations

import re
from pathlib import Path

import pytest

from core.compiler.cfg import build_cfg
from core.compiler.codegen import codegen_module, format_module_asm
from core.compiler.lowering import lower_to_ir

# Paths

_TESTS_DIR = Path(__file__).parent
_SNIPPETS_DIR = _TESTS_DIR / "bytecode_snippets"
_REFERENCE_DIR = _TESTS_DIR / "bytecode_reference"

# Prefer Tcl 9.0, fall back to 8.6, then 8.5
_REFERENCE_VERSIONS = ["9.0", "8.6", "8.5"]


def _find_reference_dir() -> Path | None:
    """Return the best available reference directory, or None."""
    for ver in _REFERENCE_VERSIONS:
        d = _REFERENCE_DIR / ver
        if d.is_dir() and any(d.glob("*.disasm")):
            return d
    return None


# Normalisation

# Match instruction lines: leading whitespace, (offset), opcode, operands, # comment
_INSTR_RE = re.compile(r"^\s+\((\d+)\)\s+(\S+)\s*(.*?)$")
# Match literal table entries (our explicit Literals: section)
_LITERAL_RE = re.compile(r'^\s+(\d+):\s+"(.*)"$')
# Match push instruction with literal comment: push1 <idx> ... # "value"
_PUSH_LIT_RE = re.compile(r'^\s+\(\d+\)\s+push[14]\s+(\d+)\s+.*?#\s*"(.*)"')
# Match local variable entries
_LVT_RE = re.compile(r"^\s+%v(\d+):\s+(.+)$")


def _normalise_instructions(disasm_text: str) -> list[str]:
    """Extract normalised instruction lines from disassembly text.

    Returns a list of strings like:
        ``(0) push1 0``
        ``(2) storeScalar1 %v0``
        ``(4) pop``
        ``(5) done``

    For ``push1``/``push4`` instructions, renumbers literal pool indices
    to a canonical form based on order of first appearance of each
    unique value.  This makes the comparison independent of literal pool
    deduplication strategy (our compiler deduplicates duplicate literals;
    tclsh sometimes does not).

    Strips comments and label lines, keeping only the opcode stream
    with byte offsets and operands.
    """
    # Split into per-function sections.
    func_sections: list[list[str]] = [[]]
    for line in disasm_text.splitlines():
        if line.lstrip().startswith("ByteCode "):
            func_sections.append([])
        func_sections[-1].append(line)

    lines: list[str] = []
    for section in func_sections:
        # Build value → canonical_index mapping (order of first use).
        # Brace-strip values so our ``{a b}`` and tclsh ``a b`` unify.
        val_to_canonical: dict[str, str] = {}
        next_canonical = 0
        for line in section:
            m = _PUSH_LIT_RE.match(line)
            if m:
                _idx_str, value = m.groups()
                norm_val = _strip_outer_braces(value)
                if norm_val not in val_to_canonical:
                    val_to_canonical[norm_val] = str(next_canonical)
                    next_canonical += 1

        # Extract and renumber instructions.
        for line in section:
            m = _INSTR_RE.match(line)
            if not m:
                continue
            offset, opcode, rest = m.groups()
            if opcode in ("push1", "push4"):
                cm = re.search(r'#\s*"(.*)"', rest)
                if cm:
                    norm_val = _strip_outer_braces(cm.group(1))
                    canonical = val_to_canonical.get(norm_val, "?")
                    lines.append(f"({offset}) {opcode} {canonical}")
                    continue
            # Strip inline comments (``# "value"`` or ``# var "x"``)
            rest = re.sub(r"\s*#.*$", "", rest).strip()
            if rest:
                lines.append(f"({offset}) {opcode} {rest}")
            else:
                lines.append(f"({offset}) {opcode}")
    return lines


def _normalise_literals(disasm_text: str) -> list[str]:
    """Extract literal values from ``push`` instruction comments.

    This extracts only **instruction-referenced** literals (from
    ``# "value"`` comments on ``push1``/``push4`` instructions),
    deduplicating per function by *value* rather than by pool index.
    This makes the comparison independent of literal pool deduplication
    strategy — our compiler deduplicates duplicate literals while
    tclsh sometimes does not.
    """
    lits: list[str] = []
    seen: set[tuple[int, str]] = set()
    func_idx = 0
    for line in disasm_text.splitlines():
        if line.lstrip().startswith("ByteCode "):
            func_idx += 1
            seen = set()
        m = _PUSH_LIT_RE.match(line)
        if m:
            _idx_str, value = m.groups()
            # Deduplicate by value within each function so that
            # different dedup strategies produce the same list.
            key = (func_idx, value)
            if key not in seen:
                seen.add(key)
                lits.append(f'"{value}"')
    return lits


def _literal_value(entry: str) -> str:
    """Extract the value from a normalised literal entry like ``"value"``."""
    # Strip surrounding quotes
    return entry[1:-1]


def _strip_outer_braces(value: str) -> str:
    """Strip one level of outer ``{...}`` from *value* if present.

    Our codegen wraps proc params and bodies in braces so the VM's
    push handler does not substitute ``$var``/``[cmd]``.  tclsh stores
    the raw values without braces.  This normalisation brings both
    representations into alignment.

    Handles two cases:

    1. Complete brace wrapping: ``{content}`` → ``content``
    2. Truncated brace wrapping: ``{content...`` → ``content...``
       (the closing ``}`` was truncated away by ``_esc``)
    """
    if not value or value[0] != "{":
        return value
    # Case 1: balanced outer braces.
    if value[-1] == "}":
        depth = 0
        for i, ch in enumerate(value):
            if ch == "{":
                depth += 1
            elif ch == "}":
                depth -= 1
            if depth == 0:
                # Only strip if the LAST } closes the first {.
                if i == len(value) - 1:
                    return value[1:-1]
                break
    # Case 2: truncated — starts with { but ends with ...
    if value.endswith("..."):
        return value[1:]
    return value


def _literals_equivalent(ours: list[str], refs: list[str]) -> tuple[bool, str]:
    """Compare literal lists with optional brace-stripping and truncation.

    Some of our codegen paths still wrap literals in ``{...}`` while
    tclsh stores them raw.  We try an exact match first, then fall back
    to stripping ONE level of outer braces from **our** value.  Either
    side may also truncate long values with ``...``.

    Returns ``(match, detail)`` where *detail* describes the first
    mismatch (empty string on match).
    """
    if len(ours) != len(refs):
        return False, f"length {len(ours)} vs {len(refs)}"
    for i, (o, r) in enumerate(zip(ours, refs)):
        o_val = _literal_value(o)
        r_val = _literal_value(r)
        if o_val == r_val:
            continue
        # Try stripping one level of outer braces from our value.
        o_stripped = _strip_outer_braces(o_val)
        if o_stripped == r_val:
            continue
        # Handle truncation on either side (using stripped value).
        for o_v in (o_val, o_stripped):
            o_trunc = o_v.endswith("...")
            r_trunc = r_val.endswith("...")
            if o_trunc or r_trunc:
                o_core = o_v[:-3] if o_trunc else o_v
                r_core = r_val[:-3] if r_trunc else r_val
                shorter = min(len(o_core), len(r_core))
                if shorter > 0 and o_core[:shorter] == r_core[:shorter]:
                    break
        else:
            return False, f"value mismatch at [{i}]: {o!r} vs {r!r}"
    return True, ""


def _normalise_lvt(disasm_text: str) -> list[str]:
    """Extract local variable table entries."""
    entries: list[str] = []
    for line in disasm_text.splitlines():
        m = _LVT_RE.match(line)
        if m:
            idx, rest = m.groups()
            entries.append(f"%v{idx}: {rest}")
    return entries


def _sort_proc_sections(disasm_text: str) -> str:
    """Sort proc-body sections so comparison is independent of iteration order.

    Tcl's ``info procs`` returns procs in hash-table iteration order which
    varies across interpreter builds.  We split the disassembly at each
    ``ByteCode`` header, keep the first section (top-level) in place, and
    sort the remaining proc-body sections by their normalised instruction
    content so both sides use the same canonical order.
    """
    sections: list[str] = []
    current: list[str] = []
    for line in disasm_text.splitlines():
        if line.startswith("ByteCode ") and current:
            sections.append("\n".join(current))
            current = []
        current.append(line)
    if current:
        sections.append("\n".join(current))
    if len(sections) <= 1:
        return disasm_text
    proc_sections = sorted(sections[1:], key=lambda s: _normalise_instructions(s))
    return "\n\n".join([sections[0]] + proc_sections)


def _extract_sections(disasm_text: str) -> dict[str, str]:
    """Split a multi-function disassembly into per-function sections.

    Returns a dict mapping function name (e.g. "::top", "::add") to
    the disassembly text for that function.
    """
    sections: dict[str, str] = {}
    current_name: str | None = None
    current_lines: list[str] = []

    for line in disasm_text.splitlines():
        if line.startswith("ByteCode "):
            if current_name is not None:
                sections[current_name] = "\n".join(current_lines)
            # Extract name: "ByteCode ::top, ..." or "ByteCode 0x..., ..."
            parts = line.split(",", 1)
            name_part = parts[0].replace("ByteCode ", "").strip()
            # Tcl uses hex addresses; ours uses qualified names
            current_name = name_part
            current_lines = [line]
        elif current_name is not None:
            current_lines.append(line)

    if current_name is not None:
        sections[current_name] = "\n".join(current_lines)

    return sections


# Our codegen


def _our_disasm(source: str) -> str:
    """Compile source with our pipeline and return disassembly text."""
    ir = lower_to_ir(source)
    cfg = build_cfg(ir, defer_top_level=True)
    module = codegen_module(cfg, ir)
    return format_module_asm(module)


# Tcl reference normalisation

# Tcl's disassemble output has extra sections we need to skip:
#   - "Source ..." line
#   - "Cmds N, src M, ..." stats line
#   - "Proc 0x..., ..." proc info
#   - "slot N, scalar, ..." compiled local descriptions
#   - "Commands N:" / "N: pc M-P, src Q-R" command map
#   - "Command N: ..." source text
#   - "Exception ranges ..." exception range table

_TCL_SKIP_PREFIXES = (
    "  Source ",
    "  Cmds ",
    "  Proc ",
    "      slot ",
    "  Commands ",
    "  Command ",
    "  Exception",
    "      ",  # exception range detail lines start with 6+ spaces
)


def _normalise_tcl_disasm(tcl_text: str) -> str:
    """Normalise Tcl reference disassembly to match our format.

    Strips Tcl-specific metadata sections, keeping only the literal
    table, local variable table, and instruction stream.
    """
    lines: list[str] = []
    in_commands_section = False
    in_exception_section = False

    for line in tcl_text.splitlines():
        # Skip the ByteCode header (we compare instructions only)
        if line.startswith("ByteCode "):
            continue

        # Detect and skip "Commands N:" section
        if re.match(r"\s+Commands \d+:", line):
            in_commands_section = True
            continue
        if in_commands_section:
            if re.match(r"\s+\d+: pc \d+-\d+, src \d+-\d+", line):
                continue
            in_commands_section = False

        # Detect and skip "Command N:" source lines
        if re.match(r"\s+Command \d+:", line):
            continue

        # Detect and skip exception ranges
        if re.match(r"\s+Exception ranges? \d+", line):
            in_exception_section = True
            continue
        if in_exception_section:
            if re.match(r"\s+\d+: level \d+", line):
                continue
            in_exception_section = False

        # Skip other Tcl-specific metadata
        skip = False
        for prefix in ("  Source ", "  Cmds ", "  Proc "):
            if line.startswith(prefix):
                skip = True
                break
        if skip:
            continue

        # Skip "slot N, ..." compiled local detail lines
        if re.match(r"\s+slot \d+,", line):
            continue

        lines.append(line)

    return "\n".join(lines)


# Test collection


def _collect_snippet_ids() -> list[str]:
    """Return sorted list of snippet basenames (without .tcl)."""
    if not _SNIPPETS_DIR.is_dir():
        return []
    return sorted(p.stem for p in _SNIPPETS_DIR.glob("*.tcl"))


_SNIPPET_IDS = _collect_snippet_ids()
_REF_DIR = _find_reference_dir()

# Snippets where our instruction stream does not yet match tclsh 9.0.
# As our compiler improves, remove entries and the tests will enforce
# the match going forward (any unexpected regression becomes a failure).
_KNOWN_INSTRUCTION_MISMATCHES: frozenset[str] = frozenset(set())

# Snippets where our literal table does not yet match tclsh 9.0.
_KNOWN_LITERAL_MISMATCHES: frozenset[str] = frozenset(set())


def _maybe_xfail(
    snippet_id: str, known_set: frozenset[str], request: pytest.FixtureRequest
) -> None:
    """Mark the test as xfail if the snippet is in the known mismatch set.

    Uses ``strict=True`` so that if a snippet unexpectedly starts passing
    (because we improved the compiler), the test *fails* (XPASS) —
    prompting us to remove it from the known-mismatch set.
    """
    if snippet_id in known_set:
        request.node.add_marker(
            pytest.mark.xfail(
                strict=True,
                reason=f"{snippet_id}: known mismatch (remove when fixed)",
            )
        )


@pytest.mark.parametrize("snippet_id", _SNIPPET_IDS)
class TestBytecodeInstructionIdentity:
    """Compare instruction streams between our codegen and tclsh reference."""

    def test_instructions_match(self, snippet_id: str, request: pytest.FixtureRequest) -> None:
        """Assert that our instruction stream matches the reference."""
        if _REF_DIR is None:
            pytest.skip("no reference bytecode available (run capture_reference_bytecode.sh)")

        ref_file = _REF_DIR / f"{snippet_id}.disasm"
        if not ref_file.exists():
            pytest.skip(f"no reference file: {ref_file}")

        _maybe_xfail(snippet_id, _KNOWN_INSTRUCTION_MISMATCHES, request)

        snippet_file = _SNIPPETS_DIR / f"{snippet_id}.tcl"
        source = snippet_file.read_text()

        # Our disassembly (sort proc sections for order-independence)
        our_text = _sort_proc_sections(_our_disasm(source))
        our_instructions = _normalise_instructions(our_text)

        # Reference disassembly (sort proc sections for order-independence)
        ref_text = _sort_proc_sections(ref_file.read_text())
        ref_instructions = _normalise_instructions(ref_text)

        assert our_instructions == ref_instructions, (
            f"Instruction mismatch for {snippet_id}:\n"
            f"  OURS: {our_instructions}\n"
            f"  REF:  {ref_instructions}"
        )


@pytest.mark.parametrize("snippet_id", _SNIPPET_IDS)
class TestBytecodeLiteralIdentity:
    """Compare literal tables between our codegen and tclsh reference."""

    def test_literals_match(self, snippet_id: str, request: pytest.FixtureRequest) -> None:
        """Assert that our literal table matches the reference."""
        if _REF_DIR is None:
            pytest.skip("no reference bytecode available (run capture_reference_bytecode.sh)")

        ref_file = _REF_DIR / f"{snippet_id}.disasm"
        if not ref_file.exists():
            pytest.skip(f"no reference file: {ref_file}")

        _maybe_xfail(snippet_id, _KNOWN_LITERAL_MISMATCHES, request)

        snippet_file = _SNIPPETS_DIR / f"{snippet_id}.tcl"
        source = snippet_file.read_text()

        our_text = _sort_proc_sections(_our_disasm(source))
        our_lits = _normalise_literals(our_text)

        ref_text = _sort_proc_sections(ref_file.read_text())
        ref_lits = _normalise_literals(ref_text)

        match, detail = _literals_equivalent(our_lits, ref_lits)
        assert match, (
            f"Literal table mismatch for {snippet_id} ({detail}):\n"
            f"  OURS: {our_lits}\n  REF:  {ref_lits}"
        )


# Self-test: verify our codegen produces parseable output


@pytest.mark.parametrize("snippet_id", _SNIPPET_IDS)
class TestCodegenSmokeTest:
    """Verify our codegen runs without errors on every snippet."""

    def test_compiles(self, snippet_id: str) -> None:
        snippet_file = _SNIPPETS_DIR / f"{snippet_id}.tcl"
        source = snippet_file.read_text()
        text = _our_disasm(source)
        # Should have at least one instruction
        instructions = _normalise_instructions(text)
        assert len(instructions) > 0, f"no instructions for {snippet_id}"
