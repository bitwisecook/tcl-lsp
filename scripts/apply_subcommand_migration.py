#!/usr/bin/env python3
"""Apply SubCommand migration to command definition files.

Reads the migration data generated by migrate_subcommands.py and inserts
the `subcommands={...}` dict into each command's spec() method.

Usage:
    python scripts/apply_subcommand_migration.py
"""

from __future__ import annotations

import re
import sys
from pathlib import Path

# Add project root to path
sys.path.insert(0, str(Path(__file__).parent.parent))


def find_command_file(cmd_name: str) -> Path | None:
    """Find the Python file defining a command."""
    base = Path("core/commands/registry")

    for dialect in ["tcl", "irules", "iapps", "tcllib", "stdlib", "tk"]:
        dialect_dir = base / dialect
        if not dialect_dir.is_dir():
            continue
        for py_file in dialect_dir.glob("*.py"):
            if py_file.name.startswith("_"):
                continue
            content = py_file.read_text()
            # Check if this file defines the command
            pattern = rf'name\s*=\s*["\']({re.escape(cmd_name)})["\']'
            if re.search(pattern, content):
                return py_file
    return None


def insert_subcommands_into_file(filepath: Path, subcommands_code: str, cmd_name: str) -> bool:
    """Insert subcommands dict into a command file's spec() method."""
    content = filepath.read_text()

    # Check if SubCommand import is needed
    needs_subcommand_import = "SubCommand" not in content
    needs_argrole_import = "ArgRole" in subcommands_code and "ArgRole" not in content
    needs_tcltype_import = "TclType" in subcommands_code and "TclType" not in content

    # Find the CommandSpec( constructor and insert subcommands before the closing )
    # Strategy: find `validation=ValidationSpec(` and insert subcommands before it
    # Or find the last field before the closing `)` of CommandSpec

    # First check if SubCommand-based subcommands already added
    # (distinguish from ValidationSpec's subcommands= field)
    if "SubCommand(" in content:
        print(f"  SKIP {filepath.name}: already has SubCommand entries")
        return False

    # Find the spec() method's CommandSpec( ... ) block
    # Insert subcommands= before `validation=` or before the closing `)` of CommandSpec
    # We'll insert after `pure_subcommands=` or `cse_candidate=` or similar

    # Strategy: find `validation=ValidationSpec(` and insert subcommands= right after
    # the `),` that closes CommandSpec (but before the end of spec())

    # Actually, let's find a good insertion point. The subcommands= should go
    # after the existing fields. Let me insert it just before the closing `)` of
    # CommandSpec, which is typically the last `)` before `@classmethod` or end of method.

    # For simplicity, insert the subcommands dict right before the
    # `validation=ValidationSpec(` line, since it logically groups with validation.

    # Find the pattern: starts with spaces + `validation=ValidationSpec(`
    validation_match = re.search(
        r"^(\s+)(validation=ValidationSpec\()",
        content,
        re.MULTILINE,
    )

    if validation_match:
        indent = validation_match.group(1)
        insert_pos = validation_match.start()
        # Insert subcommands before validation
        new_content = content[:insert_pos] + subcommands_code + "\n" + indent + content[insert_pos:]
    else:
        # Try inserting before the closing `) ` of CommandSpec
        # Find `return CommandSpec(` and its matching `)`
        print(f"  WARN {filepath.name}: no validation= found, skipping")
        return False

    # Add imports if needed
    if needs_subcommand_import:
        # Add SubCommand to the models import.
        # Parenthesised multi-line form: from ..models import (\n    ...\n)
        if re.search(r"from \.\.?models import \(", new_content):
            new_content = re.sub(
                r"(from \.\.?models import \((?:[^)]*?))(,?\s*\))",
                lambda m: m.group(1) + ",\n    SubCommand" + m.group(2),
                new_content,
                count=1,
            )
        elif "from ..models import" in new_content or "from .models import" in new_content:
            # Single-line form: from ..models import Foo, Bar
            new_content = re.sub(
                r"(from \.\.?models import )",
                r"\1SubCommand, ",
                new_content,
                count=1,
            )

    if needs_tcltype_import and "from ....compiler.types import TclType" not in new_content:
        # Add TclType import (in TYPE_CHECKING or directly)
        if "from __future__ import annotations" in new_content:
            # Add after existing imports
            if (
                "from ....compiler.types import" in new_content
                or "from ...compiler.types import" in new_content
            ):
                pass  # already imported
            else:
                # Find last import line and add after it
                import_lines = list(
                    re.finditer(r"^(from \S+ import|import \S+)", new_content, re.MULTILINE)
                )
                if import_lines:
                    last_import = import_lines[-1]
                    insert_after = last_import.end()
                    # Find end of line
                    eol = new_content.index("\n", insert_after)
                    new_content = (
                        new_content[: eol + 1]
                        + "from ....compiler.types import TclType\n"
                        + new_content[eol + 1 :]
                    )

    if needs_argrole_import:
        if "from ..signatures import" in new_content or "from .signatures import" in new_content:
            if "ArgRole" not in new_content:
                new_content = re.sub(
                    r"(from \.\.?signatures import )(.*)",
                    r"\1ArgRole, \2",
                    new_content,
                    count=1,
                )

    filepath.write_text(new_content)
    return True


def main():
    from core.commands.registry.command_registry import REGISTRY
    from core.commands.registry.runtime import configure_signatures

    configure_signatures()

    # Import the migration generator
    from scripts.migrate_subcommands import generate_subcommands_for

    migrated = 0
    skipped = 0
    failed = 0

    for name, specs in sorted(REGISTRY.specs_by_name.items()):
        spec = REGISTRY.get_any(name)
        if not spec or not spec.validation or not spec.validation.subcommands:
            continue

        filepath = find_command_file(name)
        if filepath is None:
            print(f"  FAIL {name}: file not found")
            failed += 1
            continue

        code = generate_subcommands_for(name)
        if code is None:
            continue

        result = insert_subcommands_into_file(filepath, code, name)
        if result:
            print(f"  OK   {filepath}")
            migrated += 1
        else:
            skipped += 1

    print(f"\nMigrated: {migrated}, Skipped: {skipped}, Failed: {failed}")


if __name__ == "__main__":
    main()
